import re, datetime
N = int(input())
t0 = datetime.datetime.strptime('0:0:0 1.1.2000', '%H:%M:%S %d.%m.%Y')
t1 = datetime.datetime.strptime('0:0:0 1.1.2400', '%H:%M:%S %d.%m.%Y')
s1 = (t1 - t0).total_seconds()
for i in range(N):
    t_mat = re.match('(.*\.)(.*)', input())
    y1, y2 = divmod(int(t_mat.group(2)), 400)
    t2 = datetime.datetime.strptime(t_mat.group(1) + str(2000+y2), '%H:%M:%S %d.%m.%Y')
    t = int(((t2 - t0).total_seconds() + s1 * (y1 - 5)) * 100000 / 86400)
    t, S = divmod(t, 100)
    t, M = divmod(t, 100)
    t, H = divmod(t, 10)
    t, d = divmod(t, 100)
    Y, m = divmod(t, 10)
    print('%d:%d:%d %d.%d.%d'%(H, M, S, d+1, m+1, Y))
